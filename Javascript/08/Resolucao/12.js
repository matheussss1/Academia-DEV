function getHigherAndLowerValue(dict) {
  let higherValue = - Infinity;
  let lowerValue = Infinity;
  let higherKey = '';
  let lowerKey = '';

  for (key in dict) {
    if (dict[key] > higherValue) {
      higherValue = dict[key];
      higherKey = key;
    }

    if (dict[key] < lowerValue) {
      lowerValue = dict[key];
      lowerKey = key;
    }
  }
  return [lowerKey, lowerValue, higherKey, higherValue];
}

